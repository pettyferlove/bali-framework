package com.github.bali.example.web;

import org.springframework.beans.factory.annotation.Value;
import org.springframework.security.core.Authentication;
import org.springframework.security.core.annotation.AuthenticationPrincipal;
import org.springframework.security.oauth2.core.user.OAuth2User;
import org.springframework.stereotype.Controller;
import org.springframework.ui.Model;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.PostMapping;

/**
 * @author Joe Grandja
 */
@Controller
public class AuthorizationController {

	@GetMapping("/authorized")		// registered redirect_uri for authorization_code
	public String authorized(Model model, @AuthenticationPrincipal OAuth2User principal) {
		System.out.println(principal);
		model.addAttribute("messages", "success");
		return "index";
	}
}